def addition(a,b,c=0,d=0,e=0,f=0):
    result = a+b+c+d+e+f
    return result

def subtraction(a,b,c=0,d=0,e=0,f=0):
    result= a-b-c-d-e-f
    return result


def multiplication(a,b,c=1,d=1,e=1,f=1):
    result = a*b*c*d*e*f
    return result

def division(a,b):
    result = a/b
    return result


def Simple_Interest(Principal, Rate,Time):
    Interest = Principal * Rate * Time
    return Interest

    












